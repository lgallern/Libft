#include "libft.h"

int	ft_atoi(const char *str)
{
	int n;
	int sign;

	n = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		sign = -1;
		str++;
	}
	while (*str && ft_isdigit(*str))
		n = n * 10 + *str++ - '0';
	return (n * sign);
}
